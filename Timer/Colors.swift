import Foundation
import UIKit

extension UIColor {
    
    static func lightCyan() -> UIColor {
        return UIColor(red:0.000, green:0.992, blue:0.792, alpha:1.00)
    }
    
    static func darkGrey() -> UIColor {
        return UIColor(red:0.220, green:0.212, blue:0.208, alpha:1.00)
    }
    
    static func hotPink() -> UIColor {
        return UIColor(red:0.812, green:0.000, blue:0.267, alpha:1.00)
    }
    
    static func mellowOrange() -> UIColor {
        return UIColor(red:1.000, green:0.812, blue:0.400, alpha:1.00)
    }
    
    static func brightYellow() -> UIColor {
        return UIColor(red:1.000, green:0.835, blue:0.196, alpha:1.00)
    }
}